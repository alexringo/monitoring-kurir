    private void update_gcm(){


        SharedPreferences sharedPreferences = getSharedPreferences(Config.SHARED_PREF_NAME, Context.MODE_PRIVATE);
        String user = sharedPreferences.getString(Config.USER_SHARED_PREF, "Not Available");
        String token = sharedPreferences.getString(Config.GCM_SHARED_PREF, "Not Available");

        Map<String,String> params = new HashMap<String,String>();
        params.put("user", user);
        params.put("token", token);

        //update gcm_token di tabel kurir
        CustomRequest update_token = new CustomRequest(Request.Method.POST,Config.GCM_URL,params,new Response.Listener<JSONObject>(){

            @Override
            public void onResponse(JSONObject response) {

                try {
                    int success = response.getInt("success");

                    if (success == 1) {
                        Toast.makeText(getApplicationContext(),
                                "Token Updated", Toast.LENGTH_SHORT)
                                .show();

                    } else {
                        Toast.makeText(getApplicationContext(),
                                "Token Updated", Toast.LENGTH_SHORT)
                                .show();
                    }

                } catch (JSONException e) {
                    e.printStackTrace();
                }

            }
        }, new Response.ErrorListener() {

            @Override
            public void onErrorResponse(VolleyError error) {

            }
        });

        //Adding the string request to the queue
        VolleySingleton.getInstance(this).addToRequestQueue(update_token);
    }

